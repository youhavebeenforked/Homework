package ru.sberbank.homework.kudryavykh;

public class SimpleCalculator{

    public static int add(int one, int two) {
        return one + two;
    }

    public static double add(double one, double two) {
        return one + two;
    }

    public static int substract(int one, int two) {
        return one - two;
    }

    public static double subtract(double one, double two) {
        return one - two;
    }

    public static int multiply(int one, int two) {
        return one * two;
    }

    public static double multiply(double one, double two) {
        return one * two;
    }

    public static int divide(int one, int two) {
        return one / two;
    }

    public static double divide(double one, double two) {
        return one / two;
    }
}
